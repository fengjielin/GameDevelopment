namespace Lesson16_条件分支语句_if
{
    internal class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("条件分支语句_if");

            #region 知识点一 基本语法
            // 条件分支语句 是根据 条件表达式 的结果 来决定 执行哪一段代码的语句
            // 让顺序执行 变成 分支执行
            #endregion

            #region 知识点二 if 语句
            // 作用 满足条件时 执行某段代码
            // 语法格式
            // if (条件表达式)
            // {
            //     语句体;
            // }
            // 1、条件表达式 必须是 布尔值 true 或 false
            // 2、语句体 可以是一条语句 或 多条语句 但多条语句时 必须用 大括号 包含起来
            // 3、当 条件表达式 的结果为 true 时 执行 语句体 内的代码 否则跳过
            #endregion

            #region 知识点三 if...else 语句
            // 作用 根据条件的不同 执行不同的代码
            #endregion

            #region 知识点四 if...else if...else 语句
            // 作用 根据多个条件的不同 执行不同的代码
            #endregion
        }
    }
}
